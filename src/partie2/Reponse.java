package partie2;

public class Reponse
{
    private int b;
    private int m;

    public Reponse()
    {
        this.b = 0;
        this.m = 0;
    }

    public int getB()
    {
        return b;
    }

    public int getM()
    {
        return m;
    }

    public void setB(int b)
    {
        this.b = b;
    }

    public void setM(int m)
    {
        this.m = m;
    }
}
